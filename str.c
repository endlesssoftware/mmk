int strcasecmp(s1, s2)
	register char *s1, *s2;
{
	register char *cm = charmap;

	if ((s1 == 0) && (s2 == 0))
	    return(0);
	else
	    {
	    if (s1 == 0)
		return (-1);
	    else
		if (s2 == 0)
		    return (1);
	    }

	while (cm[(unsigned char) *s1] == cm[(unsigned char) *s2++])
		if (*s1++ == '\0')
			return(0);
	return(cm[(unsigned char) *s1] - cm[(unsigned char) *--s2]);
}

int strncasecmp(s1, s2, n)
	register char *s1, *s2;
	register int n;
{
	register char *cm = charmap;

	if (n <= 0)
	    return(0);

	if ((s1 == 0) && (s2 == 0))
	    return(0);
	else
	    {
	    if (s1 == 0)
		return (-1);
	    else
		if (s2 == 0)
		    return (1);
	    }

	while (--n >= 0 && cm[(unsigned char) *s1] == cm[(unsigned char) *s2++])
		if (*s1++ == '\0')
			return(0);
	return(n < 0 ? 0 : cm[(unsigned char) *s1] - cm[(unsigned char) *--s2]);
}

char *strcasestr(register char *string, register char *substring)
{
    register char *cm = charmap;
    char *a, *b;

    if ((string == NULL) || (substring == NULL))
	return (string);

    b = substring;

    if (*b == 0) {
        return string;
    }
    for ( ; *string != 0; string += 1) {
        if (cm[(unsigned char) *string] != cm[(unsigned char) *b]) {
            continue;
        }
        a = string;
        while (1) {
            if (*b == 0) {
                return string;
            }
            if (cm[(unsigned char) *a++] != cm[(unsigned char) *b++]) {
                break;
            }
        }
        b = substring;
    }
    return (char *) 0;
}

